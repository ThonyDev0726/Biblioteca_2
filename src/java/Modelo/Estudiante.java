package Modelo;

public class Estudiante {

    private Integer idEstudiante;
    private String estNombres;
    private String estApellidos;
    private String estCedula;
    private String estCarrera;
    private String estNivel;
    private String estParalelo;
    private String estDireccion;
    private String estTelefono;
    private String estCorreo;

    public Estudiante() {
    }

    public Integer getIdEstudiante() {
        return idEstudiante;
    }

    public void setIdEstudiante(Integer idEstudiante) {
        this.idEstudiante = idEstudiante;
    }

    public String getEstNombres() {
        return estNombres;
    }

    public void setEstNombres(String estNombres) {
        this.estNombres = estNombres;
    }

    public String getEstApellidos() {
        return estApellidos;
    }

    public void setEstApellidos(String estApellidos) {
        this.estApellidos = estApellidos;
    }

    public String getEstCedula() {
        return estCedula;
    }

    public void setEstCedula(String estCedula) {
        this.estCedula = estCedula;
    }

    public String getEstCarrera() {
        return estCarrera;
    }

    public void setEstCarrera(String estCarrera) {
        this.estCarrera = estCarrera;
    }

    public String getEstNivel() {
        return estNivel;
    }

    public void setEstNivel(String estNivel) {
        this.estNivel = estNivel;
    }

    public String getEstParalelo() {
        return estParalelo;
    }

    public void setEstParalelo(String estParalelo) {
        this.estParalelo = estParalelo;
    }

    public String getEstDireccion() {
        return estDireccion;
    }

    public void setEstDireccion(String estDireccion) {
        this.estDireccion = estDireccion;
    }

    public String getEstTelefono() {
        return estTelefono;
    }

    public void setEstTelefono(String estTelefono) {
        this.estTelefono = estTelefono;
    }

    public String getEstCorreo() {
        return estCorreo;
    }

    public void setEstCorreo(String estCorreo) {
        this.estCorreo = estCorreo;
    }

}
